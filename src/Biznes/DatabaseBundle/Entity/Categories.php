<?php

namespace Biznes\DatabaseBundle\Entity;

/**
 * Categories
 */
class Categories
{
    /**
     * @var string
     */
    private $name;

    /**
     * @var integer
     */
    private $idCategory;


    /**
     * Set name
     *
     * @param string $name
     *
     * @return Categories
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Get idCategory
     *
     * @return integer
     */
    public function getIdCategory()
    {
        return $this->idCategory;
    }
}
